SELECT
	EMP_ID "사번",
	EMP_NAME "이름",
	DECODE(SUBSTR(EMP_NO, 8 , 1), '1', 'M', '2', 'F') gender,
	SALARY "급여",
	JOB_NAME "직급명",
	DEPT_TITLE "부서명"
	
FROM
	EMPLOYEE JOIN JOB USING (JOB_CODE)
	JOIN DEPARTMENT ON (DEPT_ID = DEPT_CODE)
	
WHERE 
	SALARY BETWEEN 4000000 AND 8000000
AND
	DECODE(SUBSTR(EMP_NO, 8 , 1), '1', 'M', '2', 'F') = 'F'
ORDER BY SALARY ASC;

