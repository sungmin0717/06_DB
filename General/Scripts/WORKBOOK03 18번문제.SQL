-- 국어국문학과에서 총 평점이 가장 높은 학생의 이름과 학번을 조회하시오


SELECT 
  STUDENT_NO,
  STUDENT_NAME,
  DEPARTMENT_NAME,
  POINT
FROM 
  TB_STUDENT JOIN  TB_DEPARTMENT USING (DEPARTMENT_NO)
  JOIN TB_GRADE USING (STUDENT_NO)
  
WHERE
  DEPARTMENT_NAME = '국어국문학과'
ORDER BY POINT DESC;
  

SELECT * FROM TB_STUDENT;