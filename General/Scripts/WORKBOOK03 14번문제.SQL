-- 단, 지도교수가 없을 경우 "지도교수 미지정"으로 표시


SELECT 
  STUDENT_NAME,
  PROFESSOR_NAME ,
  DEPARTMENT_NAME
FROM 
  TB_STUDENT JOIN TB_PROFESSOR USING (DEPARTMENT_NO)
  JOIN TB_DEPARTMENT USING (DEPARTMENT_NO);

ORDER BY 



