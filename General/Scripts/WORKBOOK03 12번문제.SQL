SELECT 
  STUDENT_NAME ,
  TERM_NO TERM_NAME
FROM 
  TB_STUDENT JOIN TB_CLASS USING  (DEPARTMENT_NO)
  JOIN TB_GRADE USING (STUDENT_NO)
WHERE 
  CLASS_NAME = '인간관계론'
AND
   SUBSTR(TERM_NO,1,4) = 2007;

  
  
SELECT * FROM TB_CLASS  ;
SELECT * FROM TB_STUDENT  ;
